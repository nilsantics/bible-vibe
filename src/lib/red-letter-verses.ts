// Build the lookup Set once at module load: "bookId:chapter:verse"
const RED_LETTER_SET = new Set<string>()
for (const [book, ch, from, to] of RANGES) {
  for (let v = from; v <= to; v++) {
    RED_LETTER_SET.add(`${book}:${ch}:${v}`)
  }
}

export function isRedLetter(bookId: number, chapter: number, verse: number): boolean {
  return RED_LETTER_SET.has(`${bookId}:${chapter}:${verse}`)
}
